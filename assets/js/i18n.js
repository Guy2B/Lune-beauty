export function applyI18n(root=document){
  const lang=localStorage.getItem('lune_lang')||'de';
  const dict=I18N[lang]||I18N.de;
  root.querySelectorAll('[data-i18n]').forEach(el=>{
    const path=el.getAttribute('data-i18n').split('.');
    let ref=dict; for(const p of path){ if(ref && typeof ref==='object') ref=ref[p]; }
    if(typeof ref==='string') el.textContent=ref;
  });
}

export function initLangSwitcher(){
  document.querySelectorAll('.lang-switch [data-lang]').forEach(btn=>{
    btn.addEventListener('click',()=>{ localStorage.setItem('lune_lang', btn.dataset.lang); applyI18n(); });
  });
}

